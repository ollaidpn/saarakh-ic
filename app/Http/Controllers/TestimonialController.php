     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'nom'=>'required',
            'profession'=>'required',
            'email'=>'required',
            'mobile'=>'required',
            'temoignage'=>'required',

        ]);
        $testimonial = new Testimonial();
        $testimonial->nom=$request->input('nom');
        $testimonial->profession=$request->input('profession');
        $testimonial->email=$request->input('email');
        $testimonial->mobile=$request->input('mobile');
        $testimonial->temoignage=$request->input('temoignage');

        $testimonial->save();
        return redirect('/temoignage') ->with('success', 'Demande envoy√©e!');

    }

